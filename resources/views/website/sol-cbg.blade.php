                                        <h4>Biogas:</h4>
                                        <p>Biogas is an energy-rich gas produced by anaerobic decomposition or
                                            thermochemical conversion of biomass. Biogas is composed mostly of
                                            methane (CH4), the same compound in natural gas, and carbon dioxide (CO2).
                                            The methane content of raw (untreated) biogas may vary from
                                            40%–60%, with CO2 making up most of the remainder along with small amounts
                                            of water vapor and other gases. Biogas can be burned directly as
                                            a fuel or treated to remove the CO2 and other gases for use just like
                                            natural gas. Treated biogas may be called renewable natural gas or
                                            biomethane.manure, municipal waste, plant material, sewage, green waste or
                                            food waste</p>
                                        <h4>Purification:</h4>
                                        <p>
                                            Production of biogas could be a continuous process. The utilization of
                                            biogas as an efficient energy source depends strongly on its methane
                                            concentration. Therefore, biogas purification is essential in order to have
                                            more energy per unit volume of compressed biogas and to get rid of the
                                            corrosive effect of H2S. This can be done by compressing the gas into the
                                            cylinders, which is possible only after removing carbon dioxide (CO2),
                                            hydrogen sulfide (H2S) and water vapour. Biogas purification increases the
                                            concentration of methane in biogas, in order to have fuel of higher
                                            calorific value. This can be achieved by decreasing the concentration of
                                            carbon dioxide. Elimination of carbon dioxide from the biogas helps to
                                            increase its calorific value as well as to eliminate the greenhouse gas.
                                        </p>
                                        <h4>Compression</h4>
                                        <p>
                                            It is possible to compress raw biogas, a mixture of CO2 and methane, between
                                            150-250 bar, and transport it in composite gas cylinders (cascades).
                                        </p>

